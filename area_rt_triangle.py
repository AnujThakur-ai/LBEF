'''
Area of right angled triangle
1. ask for base and height
2. use formula
3. display

'''

#code
base = eval(input(' enter base : \t'))
height = eval(input(' enter height : \t'))
# formula
area_tr = 0.5 * base * height
# display
print(f'Area of triangle having base {base} and height {height} is {area_tr}')
