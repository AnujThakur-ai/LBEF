#string method

#changing case of string
name = 'anuj kumar thakur IS a good student.....'
print('Original string:\n',name)

#lower case
print('\nLower case:\n',name.lower())

#upper case
print('\nUpper Cse:\n',name.upper())

#title: into title case
print('\nTitle Case:\n',name.title())

#swapcase: lower to upper and upper to lower
print('\nSwapcase:\n',name.swapcase())

#capitalize: convert the first leter into upper case
print('\nCapitalize:\n',name.capitalize())

#casefold:  Implements caseless string matching
print('\ncasefold:\n',name.casefold())

#center: Pad the string with the specified character.
print('\nCenter:\n',name.center())

#counts: Returns the number of occurrences of a substring in the string.
print('\nCounts:\n',name.count())

