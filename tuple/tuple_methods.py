t = (1,2,3,4,5)
print('index of  3:', t.index(3)) # this will print the index of  value 3

print('count of 2:', t.count(2)) # this will print how many times 2 is present in the tuple

print('count of 7:', t.count(7)) # this will print how many times 7 is present in the tuple

print('1 in t:', 1 in t) # this will print True if 1 is present in the tuple otherwise False

print('7 in t:', 7 in t) # this will print True if 7 is present in the tuple otherwise False

print('4 > 2:', 4 > 2) # this will print True if 4 is greater than 2 otherwise False

print('4 < 2:', 4 < 2) # this will print True if 4 is less than 2 otherwise False

print('t + (6,7,8):', t + (6,7,8)) # this will concatenate two tuples and print the result

print('t * 2:', t * 2) # this will repeat the tuple two times and print the result

print ('len(t):', len(t)) # this will print the length of the tuple
print('max(t):', max(t)) # this will print the maximum value in the tuple
print('min(t):', min(t)) # this will print the minimum value in the tuple
print('sum(t):', sum(t)) # this will print the sum of all values in the tuple
print('sorted(t):', sorted(t)) # this will print the sorted list of the tuple values
print('reversed(t):', tuple(reversed(t))) # this will print the reversed tuple
print('tuple to list:', list(t)) # this will convert the tuple to a list and print it
